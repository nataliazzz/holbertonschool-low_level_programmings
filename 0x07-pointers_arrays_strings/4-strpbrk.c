/**
  * _strpbrk - search a string
  * @s: source string
  * @accept: accepted characters
  *
  * Return: string since the first found character
  */
char *_strpbrk(char *s, char *accept)
{
	int a = 0, b;

	while (s[a])
	{
		b = 0;

		while (accept[b])
		{
			if (s[a] == accept[b])
			{
				s += a;
				return (s);
			}

			b++;
		}

		a++;
	}

	return ('\0');
}#include "main.h"

